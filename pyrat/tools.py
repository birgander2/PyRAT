def linspace_nd(*args, num=50, endpoint=True, retstep=False, dtype=None, method=np.add.outer):
    wv = []
    for arg in args:
        if isinstance(arg, tuple):
            numn = num if len(arg) == 2 else arg[2]
            wv.append(np.linspace(arg[0], arg[1], numn, endpoint=endpoint, retstep=retstep, dtype=dtype))
    return reduce(method, wv)


def parallelexec_class(args, k, queue):
    """
    Helper function to make multiprocessing on pyrat class methods possible.
    """
    func = getattr(args[0], args[1])
    if 'args' in args[2]:
        arg = args[2]['args']
        del args[2]['args']
        res = func(arg, **args[2]), args[2]['meta']
    else:
        res = func(**args[2]), args[2]['meta']
    queue.put((k, res))


def parallelexec_method(args, k, queue):
    """
    Helper function to make multiprocessing on normal methods 'easier'.
    """
    res = args[0](args[1])
    queue.put((k, res))


def multimap(jobs, mode='class'):
    """
    Replacement of multiprocessing.map which uses Process/Queue instead of Pool.
    This is faster in data handling and has no 4GB memory limitation due to pickle.
    """

    if mode == 'class':
        worker = parallelexec_class
    elif mode == 'method':
        worker = parallelexec_method
    else:
        logging.ERROR("Unknown multimap mode")
        return

    nthreads = len(jobs)
    all_proc = []
    queue = Queue()
    for k in range(nthreads):
        p = Process(target=worker, args=(jobs[k], k, queue))
        p.start()
        all_proc.append(p)
    results = [None] * nthreads
    for k in range(nthreads):
        result = queue.get()
        results[result[0]] = result[1]
    for p in all_proc:
        p.join()
    return results
