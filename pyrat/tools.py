def deshape(shape):
    """
    Extracts layer shape and data shape from a numpy ndarray shape
    :returns: lshape, dshape
    """
    lshape = (1,)
    dshape = tuple(shape)
    if len(shape) == 2:  # normal data
        pass
    else:
        lshape = tuple(shape[0:-2])
        dshape = tuple(shape[-2:])
    return lshape, dshape


def flattenlist(d):
    """
    Flattens an abirary shaped deeply nested list object
    """
    thelist = []
    for x in d:
        if not isinstance(x, list):
            thelist += [x]
        else:
            thelist += flattenlist(x)
    return thelist


def unflattenlist(d, ref):
    """
    Unflattens a list to an abirary shaped deeply nested list object,
    following the shape of an example list provided in ref.
    """
    thelist = []
    for elem in ref:
        if not isinstance(elem, list):
            thelist.append(d.pop(0))
        else:
            thelist.append(unflattenlist(d, elem))
    return thelist


class AttrDict(dict):
    """
    dict-like class, which allows additional attribute-like access to its stored key-value pairs.
    Example:
        a = AttrDict()
        a['var1'] = 57
        print(a.var1)
        a.var2 = 59
        print(a['var2']
    :author: Andreas Reigber
    """

    def __getattr__(self, item):
        try:
            return self[item]
        except KeyError as e:
            raise AttributeError(str(e))

    def __setattr__(self, key, value):
        self[key] = value

    def __dir__(self):
        return super().__dir__() + [str(k) for k in self.keys()]


def combinations_generation(arrays, out=None):
    """
    Generate a cartesian product of input arrays.

    Parameters
    ----------
    arrays : list of array-like
        1-D arrays to form the cartesian product of.
    out : ndarray
        Array to place the cartesian product in.

    Returns
    -------
    out : ndarray
        2-D array of shape (M, len(arrays)) containing cartesian products
        formed of input arrays.

    Examples
    --------
    >>> combinations_generation(([1, 2, 3], [4, 5], [6, 7]))
    array([[1, 4, 6],
           [1, 4, 7],
           [1, 5, 6],
           [1, 5, 7],
           [2, 4, 6],
           [2, 4, 7],
           [2, 5, 6],
           [2, 5, 7],
           [3, 4, 6],
           [3, 4, 7],
           [3, 5, 6],
           [3, 5, 7]])

    """
    arrays = [np.asarray(x) for x in arrays]
    dtype = arrays[0].dtype

    n = np.prod([x.size for x in arrays])
    if out is None:
        out = np.zeros([n, len(arrays)], dtype=dtype)

    m = n / arrays[0].size
    out[:, 0] = np.repeat(arrays[0], m)
    if arrays[1:]:
        combinations_generation(arrays[1:], out=out[0:m, 1:])
        for j in range(1, arrays[0].size):
            out[j * m:(j + 1) * m, 1:] = out[0:m, 1:]
    return out


def cart2pol(x, y):
    rho = np.sqrt(x ** 2 + y ** 2)
    phi = np.arctan2(y, x)
    return rho, phi


def pol2cart(rho, phi):
    x = rho * np.cos(phi)
    y = rho * np.sin(phi)
    return x, y


